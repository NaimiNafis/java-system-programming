package jx02;

public class Factorial {

	//instance variable (zokusei)

	private int num;
	private int fact;

	//instance method

	public void setNum(int n) {
		num = n;
	}

	public int getNum() {
		return num;
	}

	public void setFact(int n) {
		fact = n;
	}

	public int getFact() {
		return fact;
	}

}
